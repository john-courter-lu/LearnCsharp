using System;

namespace ConsoleCreatures
{
  class Program
  {
    static void Main()
    {
      // ASCII art 
      Console.WriteLine(" .-.");    
      Console.WriteLine("(o o)");
      Console.WriteLine(" \\ 0 \\"); // backslash is an escape character
      Console.WriteLine("  \\   \\"); // so \\ will show up as \
      Console.WriteLine("   '~~~'"); // space makes a difference
      Console.WriteLine("Spiro the Specter");
     }
  }
}
